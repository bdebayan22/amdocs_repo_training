package assignment2;

public class SumAndAvg {

	public void result(int a,int b)
	{
		System.out.println("Sum="+(a+b)+"  Average="+(a+b)/2);
	}
	public void result(int a,int b,int c)
	{
		System.out.println("Sum="+(a+b+c)+"  Average="+(a+b+c)/2);
	}
	public void result(int a,int b,int c,int d)
	{
		System.out.println("Sum="+(a+b+c+d)+"  Average="+(a+b+c+d)/2);
	}
}
